	<p>Ce sont <strong>18 éleveurs certifiés en Agriculture Biologique</strong> qui&nbsp;:</p>
	<ul>
		<li>
			<p>Suivent le mode de production de l’Agriculture Biologique, c’est-à-dire qu’ils <strong>s’interdisent les produits chimiques de synthèse et les OGM</strong> et fondent leurs méthodes de travail sur le <strong>respect de l’environnement et du bien-être des animaux</strong>&nbsp;;</p>
		</li>
		<li>
			<p>Se sont <strong>réunis</strong> pour vous proposer une <strong>gamme de produits variée</strong> (viande de bœuf, veau, agneau, porc, saucisse, merguez) et régulière tout au long de l’année&nbsp;;</p>
		</li>
		<li>
			<p>Se sont donnés des règles encore plus strictes pour vous garantir le meilleur produit en établissant un <strong>cahier des charges exigeant</strong> sur les races, l’alimentation des animaux et la durée d’élevage&nbsp;;</p>
		</li>
		<li>
			<p>Ont mis en place des <strong>procédures qualité</strong> respectant la législation en vigueur définie par la Direction des Services Vétérinaires et de l’Hygiène Alimentaire&nbsp;(«&nbsp;maîtrise des températures&nbsp;», «&nbsp;maîtrise de la traçabilité&nbsp;»)</p>
		</li>
		<li>
			<p><strong>S’engagent</strong> à respecter tous ces critères en suivant à la fois le contrôle officiel pour la certification «&nbsp;Agriculture Biologique&nbsp;» et un contrôle de l’association sur leurs méthodes de production&nbsp;;</p>
		</li>
		<li>
			<p>Travaillent sur de <strong>petites structures</strong> ce qui leur permet d’assurer une attention particulière à leur élevage et de maintenir un tissu rural dynamique&nbsp;;</p>
		</li>
		<li>
			<p>Vous assurent une <strong>transparence</strong> et une <strong>traçabilité</strong> complète en proposant ce rapport direct producteur/consommateur et en ouvrant les portes de leurs fermes pour vous expliquer leurs méthodes de travail&nbsp;;</p>
		</li>
		<li>
			<p>Vous proposent un <strong>conditionnement</strong> des produits adaptables à vos besoins&nbsp;: viande conditionnée sous vide, emballée et étiquetée individuellement, proposée par type de pièce bouchère&nbsp;;</p>
		</li>
		<li>
			<p>Vous assurent une<strong> livraison sur place</strong>, assurée par un transporteur ou par le producteur lui-même, respectant les procédures mises en place et la règlementation en vigueur.</p>
		</li>
	</ul> 	</div>

	
