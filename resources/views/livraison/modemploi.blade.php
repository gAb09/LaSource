<h4>Créer/modifier une livraison :</h4>
<ol>
	<li><strong>Assigner les 3 dates</strong>, puis les valider.</li>
	<li><strong>Ajouter/retirer des paniers</strong> grâce à la liste des paniers (&nbsp;<i class="fa fa-btn fa-shopping-basket"></i>) ou individuellement (&nbsp;<i class="fa fa-btn fa-unlink"></i>).</li>
	<li>Ensuite et pour chaque panier, <strong>indiquer le producteur.</strong><br />
		Il est possible de modifier la liste des producteurs pour chaque panier : (&nbsp;<i class="fa fa-btn fa-edit"></i>sous la sélection du producteur).</li>
	<li>À la création d’une livraison<strong>les prix des paniers</strong> sont assignés à 0.<br />
		Pour assigner le prix de base à cette livraison, cliquer sur le champs “Prix de base”.<br />
		Pour assigner une valeur spécifique saisir une nouvelle valeur dans le champs “Prix livraison”.</li>
		<li>Valider les paniers.</li>
	<li style="color:red">Gestion des relais….</li>
	<li style="color:red">Gestion des modes de paiement.</li>
	<li style="color:red">Il est alors possible de passer à la gestion des mails.</li>
	<p>Nota : il est possible de modifier directement depuis cette page la fiche d'un panier,<br />(&nbsp;<i class="fa fa-btn fa-edit"></i>à gauche de chaque panier).</p>
</ol>
