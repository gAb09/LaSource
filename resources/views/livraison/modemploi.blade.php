<h4>Créer/modifier une livraison :</h4>
<ol class="modemploi">
	<li>Assigner les 3 dates, puis les valider</li>
	<li>Ajouter/retirer des paniers grâce à la liste des paniers ou individuellement (retrait).</li>
	<li>Ensuite et pour chaque panier, indiquer le producteur. Il est possible de modifier les producteurs liés depuis cette page.</li>
	<li>Si le prix du panier diffère du prix commun, saisir une valeur dans le champs “Prix livraison”. Sinon cliquer sur le champ prix base.</li>
	<li>Valider les paniers. Il est alors possible de passer à la gestion des mails.</li>
	<li>Nota : il est possible de modifier directement depuis cette page la fiche d'un panier ( icône <i class="fa fa-btn fa-edit fa-lg"></i> ).</li>
</ol>
