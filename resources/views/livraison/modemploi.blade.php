<h4>Créer/modifier une livraison :</h4>
<ol>
	<li>Assigner les 3 dates, puis les valider.</li>
	<li>Ajouter/retirer des paniers grâce à la liste des paniers (&nbsp;<i class="fa fa-btn fa-shopping-basket"></i>) ou individuellement (&nbsp;<i class="fa fa-btn fa-unlink"></i>).</li>
	<li>Ensuite et pour chaque panier, indiquer le producteur.<br />
		Il est possible de modifier les producteurs liés depuis cette page (&nbsp;<i class="fa fa-btn fa-edit"></i>sous la sélection du producteur).</li>
	<li>Si le prix du panier diffère du prix de base, saisir une valeur dans le champs “Prix livraison”.<br />
		Sinon cliquer sur le champ prix base pour reporter sa valeur.</li>
	<li>Valider les paniers. Il est alors possible de passer à la gestion des mails.</li>
	<li>Nota : il est possible de modifier directement depuis cette page la fiche d'un panier,<br />(&nbsp;<i class="fa fa-btn fa-edit"></i>à gauche de chaque panier).</li>
</ol>
